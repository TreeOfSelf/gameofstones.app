    for ($itmnew = 0; $itmnew < $listsize; $itmnew++)
    {
      $tmp = 0;
      for ($itm = 1; $itm < count($itmlist); $itm++)
      {
        if ($itmlist[$itm][4] < $itmlist[$tmp][4]) $tmp = $itm;      
