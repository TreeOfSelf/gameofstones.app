      }
      $newlist[$itmnew] = $itmlist[$tmp];
      $y = $tmp;
      while ($y < count($itmlist) - 1)
      {
        $itmlist[$y] = $itmlist[$y+1];
        $y++;
      }
      array_pop($itmlist);
    }
    $itmlist = $newlist; 
  }
// cached
  elseif ($sortBy==3)
  {
    for ($itmnew = 0; $itmnew < $listsize; $itmnew++)
    {
      $tmp = 0;
      for ($itm = 1; $itm < count($itmlist); $itm++)
      {
        if ($itmlist[$itm][3] < $itmlist[$tmp][3]) $tmp = $itm;      
      }
      $newlist[$itmnew] = $itmlist[$tmp];
      $y = $tmp;
