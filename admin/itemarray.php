function add_bigarrays($array1,$array2) {
  for ($x=0; $x<count($array1); $x++)
    for ($y=0; $y<count($array1[0]); $y++)
      $array1[$x][$y]=intval($array1[$x][$y])+intval($array2[$x][$y]);
  return $array1;
}

